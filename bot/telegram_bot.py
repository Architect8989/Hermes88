def _push_huggingface(
    repo_url: str, token: str, branch: str, message: str, workdir: str, repo_type: str,
) -> str:
    print(f"[push-hf] Layer 1: huggingface_hub library (repo_type={repo_type})...", file=sys.stderr)
    h = _push_hf_layer1_python_lib(repo_url, token, branch, message, workdir, repo_type)
    if h:
        print("[push-hf] Layer 1 succeeded", file=sys.stderr)
        return h

    print("[push-hf] Layer 1 failed — Layer 2: local git push with HF token...", file=sys.stderr)
    h = _push_hf_layer2_local_git(repo_url, token, branch, message, workdir)
    if h:
        print("[push-hf] Layer 2 succeeded", file=sys.stderr)
