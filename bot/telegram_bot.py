def _smart_truncate(text: str, max_chars: int = 12000) -> str:
    """Truncate stderr/stdout while preserving FAILED/ERROR summary lines."""
    if len(text) <= max_chars:
        return text
    head = text[:3000]
    tail = text[-6000:]
    key_lines = [
        line for line in text.splitlines()
        if re.match(r'^(FAILED|ERROR|AssertionError|ImportError|ModuleNotFoundError)', line)
    ]
    key_block = "\n".join(key_lines[-60:])
    combined  = head + "\n\n[...middle truncated...]\n\n" + tail
    if key_block and key_block not in combined:
        combined += "\n\n[Key failure lines]:\n" + key_block
    return combined[:max_chars]


def _find_source_imports(test_content: str, workdir: str) -> list[tuple[str, str]]:
    """Return [(rel_path, content)] for source files imported by this test file."""
    results   = []
    seen      = set()
    candidates: set[str] = set()
    for pat in [
        re.compile(r'^from\s+([\w\.]+)\s+import', re.MULTILINE),
        re.compile(r'^import\s+([\w\.]+)', re.MULTILINE),
    ]:
        for m in pat.finditer(test_content):
            candidates.add(m.group(1))

    for module in candidates:
        rel = module.replace(".", "/") + ".py"
        for prefix in ["", "src/", "lib/", "app/"]:
            abs_path = Path(workdir) / prefix / rel
            if abs_path.exists() and str(abs_path) not in seen:
                try:
                    results.append((prefix + rel, abs_path.read_text()[:3000]))
                    seen.add(str(abs_path))
                except Exception:
                    pass
                break
    return results[:4]


def _collect_failure_context(workdir: str, stderr: str, max_tests: int = 5) -> str:
    """
    Extract failing test nodeids from stderr, read those test files + their imports,
    and return a context block to inject into the self-heal prompt.
    """
    nodeids = []
    for line in stderr.splitlines():
        m = re.match(r'^(?:FAILED|ERROR)\s+([\w/.\-]+\.py(?:::[^\s]+)?)', line)
        if m:
            nodeids.append(m.group(1))
    if not nodeids:
        return ""

    nodeids = list(dict.fromkeys(nodeids))[:max_tests]
    parts   = [f"Failing tests ({len(nodeids)} shown):"]

    for nodeid in nodeids:
        test_file_rel = nodeid.split("::")[0]
        test_file_abs = Path(workdir) / test_file_rel
        parts.append(f"\n=== {nodeid} ===")
        try:
            test_content = test_file_abs.read_text()
            parts.append(f"--- {test_file_rel} ---\n{test_content[:4000]}")
            for src_rel, src_content in _find_source_imports(test_content, workdir):
                parts.append(f"--- {src_rel} ---\n{src_content}")
        except Exception as e:
            parts.append(f"[could not read {test_file_rel}: {e}]")

    return "\n".join(parts)


def _get_failing_nodeids(test_cmd: str, workdir: str) -> list[str]:
    """Run test suite with --tb=no to get just the failing nodeids, quickly."""
    if "pytest" in test_cmd:
        quick_cmd = re.sub(r'\bpytest\b', 'pytest --tb=no -q', test_cmd, count=1)
    else:
        quick_cmd = test_cmd
    try:
        r = subprocess.run(
            quick_cmd, shell=True, cwd=workdir,
            capture_output=True, text=True, timeout=180
        )
        nodeids = []
        for line in (r.stdout + r.stderr).splitlines():
            m = re.match(r'^FAILED\s+([\w/.\-]+\.py(?:::[^\s]+)?)', line)
            if m:
                nodeids.append(m.group(1))
        return nodeids
    except Exception:
        return []


SELF_HEAL_PROMPT_TEMPLATE = """Command `{cmd}` failed in `{workdir}`.
You have {strikes_remaining} attempt(s) left before escalation.

Previously passing tests (do NOT regress these): {prev_passing}

## Failure Output

{stderr}

## Source Context (failing test files + their imports)

{file_context}

## Instructions

1. Read the failure output and source context above carefully.
2. Identify the root cause precisely — wrong assertion value, missing import, bad logic, type error.
3. If a Python/system package is missing: run `pip install <pkg> --quiet` via subprocess.
4. Fix the source files that caused the failure. Write ALL fixes directly to disk now.
5. Do NOT ask for confirmation. Do NOT explain. Fix and exit immediately.
6. The test suite will be re-run automatically after you exit.
7. Do NOT modify files unrelated to the failing tests.

Fix everything now."""


def run_bounded(
    cmd: str,
    workdir: str,
    max_strikes: int = 3,
    api_key: str = "",
    base_url: str = "",
    model: str = "",
    per_strike_timeout: int = 1200,   # FIX-C / FIX-F: was hardcoded 1200, now configurable
) -> dict:
    """
    Run a shell command with bounded autonomy self-healing.
    On failure: collects test context, injects file contents, invokes openclaude, retries.

    Returns {"success": bool, "output": str, "strikes_used": int, "final_stderr": str}
    """
    strikes        = 0
    last_stderr    = ""
    last_stdout    = ""
    initial_failing: list[str] = []
    prev_passing:   list[str] = []

    while strikes < max_strikes:
        try:
            # Use start_new_session=True so the shell and ALL its children
            # form a new process group. On timeout, os.killpg kills the entire tree.
            result = subprocess.run(
                cmd, shell=True, cwd=workdir,
                capture_output=True, text=True,
                timeout=per_strike_timeout,   # FIX-F: was hardcoded 1200
                start_new_session=True,       # FIX-16: enables process group kill
            )
        except subprocess.TimeoutExpired as te:
            # Kill the entire process group (shell + children) on timeout
            import signal
            if hasattr(te, 'args') and te.args:
                pass  # subprocess.run already killed the direct child
            try:
                import os as _os
                # The process group ID equals the PID of the session leader (the shell)
                # subprocess.run with start_new_session=True guarantees pgid == child pid
                _os.killpg(_os.getpgid(0), signal.SIGKILL)
            except (ProcessLookupError, PermissionError, OSError):
                pass  # Process already dead or we don't own it
            strikes += 1
            last_stderr = (
                f"[bounded-run] Command timed out after {per_strike_timeout}s "
                f"(strike {strikes}/{max_strikes})"
            )
            print(last_stderr, file=sys.stderr)
            continue

        last_stdout = result.stdout
        last_stderr = result.stderr or ""

        if result.returncode == 0:
            return {
                "success":      True,
                "output":       last_stdout,
                "strikes_used": strikes,
                "final_stderr": "",
            }

        strikes  += 1
        combined  = _smart_truncate(last_stderr + "\n" + last_stdout).strip()
        print(
            f"[bounded-run] Strike {strikes}/{max_strikes} — exit {result.returncode}",
            file=sys.stderr,
        )
        print(f"[bounded-run] Tail: {combined[-400:]}", file=sys.stderr)

        if strikes >= max_strikes:
            break

        current_failing = _get_failing_nodeids(cmd, workdir)
        if not initial_failing:
            initial_failing = current_failing
        if prev_passing:
            new_fixed = [
                t for t in initial_failing
                if t not in current_failing and t not in prev_passing
            ]
            prev_passing.extend(new_fixed)
        else:
            prev_passing = [t for t in initial_failing if t not in current_failing]

        _self_heal(
            cmd=cmd, workdir=workdir,
            stderr=combined,
            strikes_remaining=max_strikes - strikes,
            api_key=api_key, base_url=base_url, model=model,
            prev_passing=prev_passing,
        )

    # Escalation: jcode swarm for parallel per-module healing after all strikes exhausted
    failures_json = "/tmp/failures.json"
    failures = _get_failing_nodeids(cmd, workdir)
    if failures:
        import json as _json
        # Use the workdir (git repo root) as the clone target — not individual test file paths
        unique_modules = list(dict.fromkeys(f.split("::")[0] for f in failures))
        Path(failures_json).write_text(_json.dumps(
            [f"file://{workdir}" for _ in unique_modules[:3]]
        ))
        print(f"[bounded-run] Escalating {len(failures)} failures to jcode swarm", file=sys.stderr)
        subprocess.run([
            "python3", "/app/skills/jcode_swarm/spawn.py",
            "--repos", failures_json,
            "--task", (
                f"Fix failing tests in this repo. "
                f"Failing modules: {', '.join(unique_modules[:3])}. "
                f"Command was: {cmd}. Read every import. Write fixes to disk."
            ),
            "--workers", "3",
        ], timeout=1800)

    return {
        "success":      False,
        "output":       last_stdout,
        "strikes_used": strikes,
        "final_stderr": last_stderr,
    }


def _self_heal(
    cmd: str, workdir: str, stderr: str, strikes_remaining: int,
    api_key: str, base_url: str, model: str,
    prev_passing: list[str] | None = None,
):
    """
    Invoke openclaude to fix failing code.
    FIX-G: openclaude stderr now surfaces to our stderr so real errors are visible.
    """
    file_context     = _collect_failure_context(workdir, stderr)
    truncated_stderr = _smart_truncate(stderr, max_chars=12000)
    prev_str         = ", ".join(prev_passing[:20]) if prev_passing else "none tracked yet"

    prompt = SELF_HEAL_PROMPT_TEMPLATE.format(
        cmd=cmd,
        workdir=workdir,
        stderr=truncated_stderr,
        file_context=file_context or "(no test files parsed — check workdir and test format)",
        strikes_remaining=strikes_remaining,
        prev_passing=prev_str,
    )

    providers = [
        {
            "name":     "DO Inference",
            "base_url": base_url or os.getenv("DO_INFERENCE_BASE_URL", "https://inference.do-ai.run/v1"),
            "api_key":  api_key or os.getenv("DO_INFERENCE_API_KEY", ""),
            "model":    model or os.getenv("OPENCLAUDE_MODEL", "deepseek-v4-pro"),
        },
        {
            "name":     "DO Inference (kimi-k2.6 fallback)",
            "base_url": os.getenv("DO_INFERENCE_BASE_URL", "https://inference.do-ai.run/v1"),
            "api_key":  os.getenv("DO_INFERENCE_API_KEY", ""),
            "model":    "kimi-k2.6",
        },
    ]

    for provider in providers:
        if not provider["api_key"]:
            print(f"[self-heal] Skipping {provider['name']} — no API key", file=sys.stderr)
            continue

        env = {
            **os.environ,
            "CLAUDE_CODE_USE_OPENAI": "1",
            "OPENAI_BASE_URL":         provider["base_url"],
            "OPENAI_API_KEY":          provider["api_key"],
            "NVIDIA_API_KEY":          provider["api_key"],
            "OPENAI_MODEL":            provider["model"],
        }

        print(
            f"[self-heal] Strike remaining={strikes_remaining} "
            f"| provider={provider['name']} | context_chars={len(file_context)} "
            f"| protected_tests={len(prev_passing or [])}",
            file=sys.stderr,
        )
        try:
            result = subprocess.run(
                ["python3", "/app/skills/openclaude_grpc/client.py",
                 "--prompt", prompt,
                 "--workdir", workdir,
                 "--model", provider.get("model") or os.getenv("OPENAI_MODEL", "deepseek-v4-pro"),
                 "--timeout", "480"],
                cwd=workdir, env=env,
                capture_output=True, text=True,
                timeout=510,
            )
            # Surface gRPC client stderr so real errors are visible
            if result.stderr.strip():
                print(f"[self-heal] grpc-client stderr:\n{result.stderr[-1000:]}", file=sys.stderr)
            if result.returncode == 0:
                print(f"[self-heal] openclaude gRPC via {provider['name']} completed", file=sys.stderr)
                return
            print(
                f"[self-heal] openclaude gRPC via {provider['name']} exited {result.returncode}",
                file=sys.stderr,
            )
        except FileNotFoundError:
            print("[self-heal] grpc client not found at /app/skills/openclaude_grpc/client.py", file=sys.stderr)
            return
        except subprocess.TimeoutExpired:
            print(f"[self-heal] openclaude gRPC via {provider['name']} timed out after 510s", file=sys.stderr)

    print("[self-heal] All providers exhausted", file=sys.stderr)


