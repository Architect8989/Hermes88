def _collect_changed_files(workdir: str) -> list[str]:
    """
    Collect changed/added/modified files relative to workdir root.

    FIX-B: three-pass strategy handles the case where openclaude commits internally:
      Pass 1: git status --porcelain  (unstaged + untracked changes)
      Pass 2: git diff --name-only HEAD~1 HEAD  (last commit vs its parent)
      Pass 3: git ls-files  (all tracked files — last resort)
    """
    # Pass 1: unstaged + untracked
    files = _run_git_file_list(
        ["git", "status", "--porcelain"], workdir, start_col=3
    )
    if files:
        return files

    # Pass 2: FIX-B — last commit vs parent (handles openclaude internal commits)
    files = _run_git_file_list(
        ["git", "diff", "--name-only", "HEAD~1", "HEAD"], workdir, start_col=0
    )
    if files:
        print(
            "[collect-files] Using HEAD~1 diff (openclaude committed internally)",
            file=sys.stderr,
        )
        return files

    # Pass 3: all tracked files (last resort for fresh repos)
    files = _run_git_file_list(
        ["git", "ls-files"], workdir, start_col=0
    )
    if files:
        print("[collect-files] Using ls-files (fresh repo)", file=sys.stderr)
    return files


def _run_git_file_list(cmd: list[str], workdir: str, start_col: int) -> list[str]:
    """Run a git command and parse file paths from its output."""
    try:
        result = subprocess.run(
            cmd, cwd=workdir, capture_output=True, text=True, timeout=30,
        )
        files = []
        for line in result.stdout.splitlines():
            if len(line) < (start_col + 1):
                continue
            fname = line[start_col:].strip() if start_col else line.strip()
            if " -> " in fname:
                fname = fname.split(" -> ")[-1].strip()
            fname = fname.strip('"')
            if fname and not fname.startswith(".git/"):
                files.append(fname)
        return files
    except Exception as e:
        print(f"[collect-files] {cmd[1]} failed: {e}", file=sys.stderr)
        return []


