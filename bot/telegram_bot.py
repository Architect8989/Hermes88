# CLI entry point
# ─────────────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(
        description="Rhodawk Hermes utility — push-commit, bounded-run, ingest-media, rotate-camofox-key, health-check"
    )
    sub = parser.add_subparsers(dest="action", required=True)

    # push-commit
    p_push = sub.add_parser("push-commit", help="Resilient git push to GitHub or HuggingFace")
    p_push.add_argument("--repo",      required=True, help="Repository URL")
    p_push.add_argument("--token",     required=True, help="Auth token (PAT or HF token)")
    p_push.add_argument("--branch",    default="main")
    p_push.add_argument("--message",   default="chore: hermes automated commit")
    p_push.add_argument("--workdir",   required=True)
    p_push.add_argument("--repo-type", default="auto",
                        choices=["auto", "space", "model", "dataset"])

    # bounded-run
    p_run = sub.add_parser("bounded-run", help="Run command with N-strike self-healing")
    p_run.add_argument("--cmd",      required=True)
    p_run.add_argument("--workdir",  required=True)
    p_run.add_argument("--strikes",  type=int, default=3)
    # FIX-F: --timeout flag now wired through to per-strike subprocess timeout
    p_run.add_argument("--timeout",  type=int, default=1200,
                       help="Per-strike timeout in seconds (default 1200)")
    p_run.add_argument("--api-key",  default=os.getenv("DO_INFERENCE_API_KEY", ""))
    p_run.add_argument("--base-url",
                       default=os.getenv("DO_INFERENCE_BASE_URL", "https://inference.do-ai.run/v1"))
    p_run.add_argument("--model",
                       default=os.getenv("OPENCLAUDE_MODEL", "deepseek-v4-pro"))

    # ingest-media
    p_ingest = sub.add_parser("ingest-media", help="Extract content from image/PDF/ZIP/text")
    p_ingest.add_argument("--path", required=True)

    # rotate-camofox-key  (FIX-I — v4.0)
    p_rotate = sub.add_parser(
        "rotate-camofox-key",
        help=(
            "Regenerate CAMOFOX_ACCESS_KEY: update /data/.hermes/.env, "
            "push to HF Space secrets, docker restart camofox"
        ),
    )
    p_rotate.add_argument(
        "--hf-token",
        default=os.getenv("HF_TOKEN", ""),
        help="HuggingFace write token (default: $HF_TOKEN)",
    )
    p_rotate.add_argument(
        "--hf-space-id",
        default=os.getenv("HF_SPACE_ID", "Architect8999/Hermes"),
        help="HuggingFace Space ID in owner/space format (default: Architect8999/Hermes)",
    )
    p_rotate.add_argument(
        "--env-path",
        default="/data/.hermes/.env",
        help="Path to the .env file to update (default: /data/.hermes/.env)",
    )
    p_rotate.add_argument(
        "--skip-docker",
        action="store_true",
        help="Skip docker restart camofox (use when docker is unavailable)",
    )

    # health-check  (FIX-J — v4.1)
    p_health = sub.add_parser(
        "health-check",
        help=(
            "Probe hermes-gateway, openclaude gRPC, camofox, and jcode in parallel. "
            "Returns JSON status card + human-readable summary."
        ),
    )
    p_health.add_argument(
        "--gateway-url",
        default=os.getenv("HERMES_GATEWAY_URL", "http://localhost:8000/health"),
        help="hermes-gateway health endpoint (default: http://localhost:8000/health)",
    )
    p_health.add_argument(
        "--grpc-host",
        default="localhost",
        help="openclaude gRPC host (default: localhost)",
    )
    p_health.add_argument(
        "--grpc-port",
        type=int,
        default=50051,
        help="openclaude gRPC port (default: 50051)",
    )
    p_health.add_argument(
        "--camofox-url",
        default="http://localhost:9377/health",
        help="camofox health endpoint (default: http://localhost:9377/health)",
    )
    p_health.add_argument(
        "--jcode-port",
        type=int,
        default=18789,
        help="jcode/openclaw TCP port to probe (default: 18789)",
    )
    p_health.add_argument(
        "--timeout",
        type=float,
        default=8.0,
        help="Per-probe timeout in seconds (default: 8.0)",
    )
    p_health.add_argument(
        "--json-only",
        action="store_true",
        help="Suppress the human-readable stderr summary, emit JSON only",
    )

    args = parser.parse_args()

    if args.action == "push-commit":
        try:
            commit_hash = push_commit(
                repo_url=args.repo,
                token=args.token,
                branch=args.branch,
                message=args.message,
                workdir=args.workdir,
                repo_type=args.repo_type,
            )
            print(json.dumps({
                "success":     True,
                "commit_hash": commit_hash,
                "platform":    _detect_repo_platform(args.repo),
            }))
        except RuntimeError as e:
            print(json.dumps({"success": False, "error": str(e)}))
            sys.exit(1)

    elif args.action == "bounded-run":
        result = run_bounded(
            cmd=args.cmd,
            workdir=args.workdir,
            max_strikes=args.strikes,
            api_key=args.api_key,
            base_url=args.base_url,
            model=args.model,
            per_strike_timeout=args.timeout,   # FIX-F
        )
