def ingest_media(path: str) -> dict:
    """
    Extract text content from image/PDF/ZIP/text files.
    FIX-H: added ZIP extraction with recursive reading of contained text files.
    Returns {"content": str, "type": str, "path": str}.
    """
    p    = Path(path)
    ext  = p.suffix.lower()
    name = p.name

    if not p.exists():
        return {"content": "", "type": "error", "path": path, "error": "File not found"}

    # Plain text / markdown / code
    if ext in (".txt", ".md", ".py", ".js", ".ts", ".json", ".yaml", ".yml",
               ".toml", ".csv", ".rst", ".html", ".xml", ".sh"):
        try:
            content = p.read_text(errors="replace")
            return {"content": content[:50000], "type": "text", "path": path}
        except Exception as e:
            return {"content": "", "type": "error", "path": path, "error": str(e)}

    # PDF — use pdfminer or pdftotext
    if ext == ".pdf":
        try:
            import pdfminer.high_level
            content = pdfminer.high_level.extract_text(path)
            return {"content": (content or "")[:50000], "type": "pdf", "path": path}
        except ImportError:
            pass
        try:
            r = subprocess.run(
                ["pdftotext", path, "-"],
                capture_output=True, text=True, timeout=60,
            )
            if r.returncode == 0:
                return {"content": r.stdout[:50000], "type": "pdf", "path": path}
        except Exception:
            pass
        return {
            "content":  "",
            "type":     "pdf",
            "path":     path,
            "error":    "pdfminer and pdftotext unavailable — install pdfminer.six",
        }

    # ZIP — extract and read text files inside  (FIX-H)
    if ext == ".zip":
        import zipfile
        import tempfile
        try:
            with tempfile.TemporaryDirectory() as tmpdir:
                with zipfile.ZipFile(path) as zf:
                    zf.extractall(tmpdir)
                parts: list[str] = []
                for fp in sorted(Path(tmpdir).rglob("*")):
                    if fp.is_file() and fp.suffix.lower() in (
                        ".py", ".js", ".ts", ".txt", ".md", ".json",
                        ".yaml", ".yml", ".toml", ".sh", ".rst", ".html",
                    ):
                        try:
                            rel  = fp.relative_to(tmpdir)
                            text = fp.read_text(errors="replace")
                            parts.append(f"=== {rel} ===\n{text[:8000]}")
                        except Exception:
                            pass
                    if sum(len(x) for x in parts) > 48000:
                        break
                return {
                    "content": "\n\n".join(parts)[:50000],
                    "type":    "zip",
                    "path":    path,
                }
        except Exception as e:
            return {"content": "", "type": "error", "path": path, "error": str(e)}

    # Image — describe via base64 embed or fallback message
    if ext in (".jpg", ".jpeg", ".png", ".gif", ".webp", ".bmp"):
        try:
            b64     = base64.b64encode(p.read_bytes()).decode()
            content = f"[Image: {name}] Base64 encoded ({len(b64)} chars). Pass to vision-capable LLM."
            return {"content": content, "type": "image", "path": path, "base64": b64[:100000]}
        except Exception as e:
            return {"content": "", "type": "error", "path": path, "error": str(e)}

    # Unknown — try reading as text
    try:
        content = p.read_text(errors="replace")
        return {"content": content[:50000], "type": "text", "path": path}
    except Exception as e:
        return {
            "content": "",
            "type":    "unknown",
            "path":    path,
            "error":   f"Unsupported file type {ext}: {e}",
        }


