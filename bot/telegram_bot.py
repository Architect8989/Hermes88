def _detect_repo_platform(repo_url: str) -> str:
    url_lower = repo_url.lower()
    if "huggingface.co" in url_lower or "hf.co" in url_lower:
        return "huggingface"
    return "github"


def _parse_hf_repo_id(repo_url: str) -> str:
    url = repo_url.rstrip("/")
    for prefix in [
        "https://huggingface.co/spaces/",
        "https://huggingface.co/datasets/",
        "https://huggingface.co/models/",
        "https://huggingface.co/",
        "https://hf.co/spaces/",
        "https://hf.co/datasets/",
        "https://hf.co/",
    ]:
        if url.startswith(prefix):
            return url[len(prefix):]
    return url.split("huggingface.co/")[-1].split("hf.co/")[-1]


def _detect_hf_repo_type(repo_url: str) -> str:
    url_lower = repo_url.lower()
    if "/spaces/"   in url_lower: return "space"
    if "/datasets/" in url_lower: return "dataset"
    if "/models/"   in url_lower: return "model"
    return "space"


def push_commit(
    repo_url: str, token: str, branch: str, message: str,
    workdir: str, repo_type: str = "auto",
) -> str:
    """
    Push local changes to a repository.
    Auto-detects GitHub vs HuggingFace from URL.
    Returns commit hash string. Raises RuntimeError if all layers fail.
    Token is NEVER written to stdout or stderr.
    """
    platform = _detect_repo_platform(repo_url)
    if platform == "huggingface":
        detected_type = _detect_hf_repo_type(repo_url) if repo_type == "auto" else repo_type
        print(f"[push] Detected HuggingFace repo (type={detected_type})", file=sys.stderr)
        return _push_huggingface(repo_url, token, branch, message, workdir, detected_type)
    else:
        print("[push] Detected GitHub repo", file=sys.stderr)
        return _push_github(repo_url, token, branch, message, workdir)


