def _push_github(repo_url: str, token: str, branch: str, message: str, workdir: str) -> str:
    print("[push-gh] Layer 1: local git push...", file=sys.stderr)
    h = _push_github_layer1_local_git(repo_url, token, branch, message, workdir)
    if h:
        print("[push-gh] Layer 1 succeeded", file=sys.stderr)
        return h

    print("[push-gh] Layer 1 failed — Layer 2: GitHub REST API (Python)...", file=sys.stderr)
    h = _push_github_layer2_api_python(repo_url, token, branch, message, workdir)
    if h:
        print("[push-gh] Layer 2 succeeded", file=sys.stderr)
        return h

    print("[push-gh] Layer 2 failed — Layer 3: GitHub REST API (Node.js)...", file=sys.stderr)
    h = _push_github_layer3_api_nodejs(repo_url, token, branch, message, workdir)
    if h:
        print("[push-gh] Layer 3 succeeded", file=sys.stderr)
        return h

    raise RuntimeError(
        "[push-gh] All 3 GitHub push layers failed.\n"
        "Verify GITHUB_PAT has 'repo' and 'workflow' scopes.\n"
        "Check that the branch exists and is not protected."
    )


def _push_github_layer1_local_git(
    repo_url: str, token: str, branch: str, message: str, workdir: str,
) -> str | None:
    """
    Local git add + commit + push using git credential.helper (token never in URL).
    FIX-E: exponential backoff retry on transient network failures.
    FIX-Layer1: token was previously embedded in the remote URL
                (https://x-token-auth:<TOKEN>@...) which leaks the secret into
                git's reflog, process list, and any push error messages.
                Now uses git credential.helper subprocess so the token only
                ever travels over the HTTPS connection, not in argv or logs.
    FIX-10: Pre-cleanup removes stale credential.helper from prior unclean shutdown
            (SIGKILL scenario where finally block never executed).
    """
    # Pre-cleanup: remove any stale credential.helper left by a prior SIGKILL
    subprocess.run(
        ["git", "config", "--unset", "credential.helper"],
        cwd=workdir, capture_output=True,
    )
    try:
        # Point origin at the clean (no-token) URL
        subprocess.run(
            ["git", "remote", "set-url", "origin", repo_url],
            cwd=workdir, capture_output=True, timeout=10,
        )
        # Inject credentials via credential.helper — token never touches argv
        cred_helper = (
            f"!f() {{ echo 'username=x-token-auth'; echo 'password={token}'; }}; f"
        )
        subprocess.run(
            ["git", "config", "credential.helper", cred_helper],
            cwd=workdir, check=True, capture_output=True, timeout=10,
        )
        subprocess.run(
            ["git", "add", "-A"],
            cwd=workdir, check=True, capture_output=True, timeout=60,
        )
        commit_r = subprocess.run(
            ["git", "commit", "-m", message],
            cwd=workdir, capture_output=True, text=True, timeout=60,
        )
        if commit_r.returncode != 0:
            combined = (commit_r.stdout + commit_r.stderr).lower()
            if "nothing to commit" in combined:
                rev = subprocess.run(
                    ["git", "rev-parse", "HEAD"],
                    cwd=workdir, capture_output=True, text=True, timeout=10,
                )
                return rev.stdout.strip() if rev.returncode == 0 else None
            print(
                f"[push-gh-L1] commit failed (rc={commit_r.returncode}): "
                f"{commit_r.stderr[:200]}",
                file=sys.stderr,
            )
            raise subprocess.CalledProcessError(commit_r.returncode, "git commit")

        # FIX-E: retry push up to 3 times with exponential backoff
        last_push_err = ""
        for attempt in range(1, 4):
            push_r = subprocess.run(
                ["git", "push", "origin", f"HEAD:{branch}"],
                cwd=workdir, capture_output=True, text=True, timeout=120,
            )
            if push_r.returncode == 0:
                rev = subprocess.run(
                    ["git", "rev-parse", "HEAD"],
                    cwd=workdir, capture_output=True, text=True, timeout=10,
                )
                return rev.stdout.strip() if rev.returncode == 0 else None
            last_push_err = push_r.stderr[:300]
            print(
                f"[push-gh-L1] push attempt {attempt}/3 failed: {last_push_err}",
                file=sys.stderr,
            )
            if attempt < 3:
                time.sleep(2 ** attempt)

        print(f"[push-gh-L1] All push retries exhausted: {last_push_err}", file=sys.stderr)
        return None
    except (subprocess.CalledProcessError, subprocess.TimeoutExpired) as e:
        print(f"[push-gh-L1] Failed: {type(e).__name__}", file=sys.stderr)
        return None
    finally:
        # Always clean up the credential helper regardless of push outcome
        subprocess.run(
            ["git", "config", "--unset", "credential.helper"],
            cwd=workdir, capture_output=True,
        )


def _push_github_layer2_api_python(
    repo_url: str, token: str, branch: str, message: str, workdir: str,
) -> str | None:
    """
    Push via GitHub REST API using requests.
    Creates blobs → new tree → new commit → updates branch ref.
    FIX-D: HTTP 422 (protected branch) is now logged with full detail.
    """
    try:
        owner_repo = repo_url.rstrip("/").replace("https://github.com/", "")
        api_base   = f"https://api.github.com/repos/{owner_repo}"
        headers    = {
            "Authorization": f"token {token}",
            "Accept":        "application/vnd.github.v3+json",
            "Content-Type":  "application/json",
            "User-Agent":    "rhodawk-hermes/3.0",
        }

        ref_r = requests.get(f"{api_base}/git/ref/heads/{branch}", headers=headers, timeout=30)
        ref_r.raise_for_status()
        base_sha = ref_r.json()["object"]["sha"]

        commit_r = requests.get(f"{api_base}/git/commits/{base_sha}", headers=headers, timeout=30)
        commit_r.raise_for_status()
        base_tree_sha = commit_r.json()["tree"]["sha"]

        changed_files = _collect_changed_files(workdir)
        if not changed_files:
            print("[push-gh-L2] No changed files — returning current HEAD", file=sys.stderr)
            return base_sha

        tree_items = []
        for filepath in changed_files:
            full_path = Path(workdir) / filepath
            if not full_path.exists() or not full_path.is_file():
                continue
            try:
                b64 = base64.b64encode(full_path.read_bytes()).decode()
            except Exception as e:
                print(f"[push-gh-L2] Skipping {filepath}: {e}", file=sys.stderr)
                continue

            blob_r = requests.post(
                f"{api_base}/git/blobs", headers=headers, timeout=30,
                json={"content": b64, "encoding": "base64"},
            )
            blob_r.raise_for_status()
            tree_items.append({
                "path": filepath, "mode": "100644",
                "type": "blob", "sha": blob_r.json()["sha"],
            })

        if not tree_items:
            return base_sha

        tree_r = requests.post(
            f"{api_base}/git/trees", headers=headers, timeout=30,
            json={"base_tree": base_tree_sha, "tree": tree_items},
        )
        tree_r.raise_for_status()

        commit_r2 = requests.post(
            f"{api_base}/git/commits", headers=headers, timeout=30,
            json={"message": message, "tree": tree_r.json()["sha"], "parents": [base_sha]},
        )
        commit_r2.raise_for_status()
        new_sha = commit_r2.json()["sha"]

        patch_r = requests.patch(
            f"{api_base}/git/refs/heads/{branch}", headers=headers, timeout=30,
            json={"sha": new_sha, "force": False},
        )

        # FIX-D: HTTP 422 = protected branch or non-fast-forward — log it explicitly
        if patch_r.status_code == 422:
            print(
                f"[push-gh-L2] Ref update rejected (422 — protected branch or "
                f"non-fast-forward): {patch_r.text[:300]}",
                file=sys.stderr,
            )
            return None

        patch_r.raise_for_status()
        return new_sha

    except requests.HTTPError as e:
        print(
            f"[push-gh-L2] HTTP {e.response.status_code}: {e.response.text[:300]}",
            file=sys.stderr,
        )
        return None
    except Exception as e:
        print(f"[push-gh-L2] GitHub API (Python) failed: {type(e).__name__}: {e}", file=sys.stderr)
        return None


def _push_github_layer3_api_nodejs(
    repo_url: str, token: str, branch: str, message: str, workdir: str,
) -> str | None:
    """Generate and execute a Node.js script that pushes via GitHub REST API."""
    changed_files = _collect_changed_files(workdir)
    file_data: dict[str, str] = {}
    for f in changed_files:
        fp = Path(workdir) / f
        if fp.exists() and fp.is_file():
            try:
                file_data[f] = base64.b64encode(fp.read_bytes()).decode()
            except Exception:
                pass

    owner_repo = repo_url.rstrip("/").replace("https://github.com/", "")

    script = f"""'use strict';
const https = require('https');
const TOKEN = {json.dumps(token)};
const OWNER_REPO = {json.dumps(owner_repo)};
const BRANCH = {json.dumps(branch)};
const MESSAGE = {json.dumps(message)};
const FILES = {json.dumps(file_data)};

function ghReq(method, path, body) {{
  return new Promise((resolve, reject) => {{
    const payload = body ? JSON.stringify(body) : null;
    const req = https.request({{
      hostname: 'api.github.com',
      path: `/repos/${{OWNER_REPO}}${{path}}`,
      method,
      headers: {{
        'Authorization': `token ${{TOKEN}}`,
        'Accept': 'application/vnd.github.v3+json',
        'Content-Type': 'application/json',
        'User-Agent': 'rhodawk-hermes/3.0',
        ...(payload ? {{'Content-Length': Buffer.byteLength(payload)}} : {{}})
      }}
    }}, res => {{
      let buf = '';
      res.on('data', c => buf += c);
      res.on('end', () => {{ try {{ resolve(JSON.parse(buf)); }} catch(e) {{ resolve(buf); }} }});
    }});
    req.on('error', reject);
    if (payload) req.write(payload);
    req.end();
  }});
}}

async function main() {{
  const ref = await ghReq('GET', `/git/ref/heads/${{BRANCH}}`);
  if (!ref.object) throw new Error('Branch not found: ' + BRANCH + ' — ' + JSON.stringify(ref));
  const baseSha = ref.object.sha;
  const baseCommit = await ghReq('GET', `/git/commits/${{baseSha}}`);
  const baseTreeSha = baseCommit.tree.sha;
  const treeItems = [];
  for (const [path, b64] of Object.entries(FILES)) {{
    const blob = await ghReq('POST', '/git/blobs', {{content: b64, encoding: 'base64'}});
    if (!blob.sha) throw new Error('Blob creation failed for: ' + path);
    treeItems.push({{path, mode: '100644', type: 'blob', sha: blob.sha}});
  }}
  if (!treeItems.length) {{ process.stdout.write(baseSha + '\\n'); process.exit(0); }}
  const newTree = await ghReq('POST', '/git/trees', {{base_tree: baseTreeSha, tree: treeItems}});
  const newCommit = await ghReq('POST', '/git/commits', {{message: MESSAGE, tree: newTree.sha, parents: [baseSha]}});
  const patchRes = await ghReq('PATCH', `/git/refs/heads/${{BRANCH}}`, {{sha: newCommit.sha, force: false}});
  if (patchRes.message) throw new Error('Ref update failed: ' + patchRes.message);
  process.stdout.write(newCommit.sha + '\\n');
}}
main().catch(e => {{ process.stderr.write('[push-gh-L3] ' + e.message + '\\n'); process.exit(1); }});
"""
    try:
        script_path = "/tmp/rhodawk_push_gh_l3.js"
        Path(script_path).write_text(script)
        result = subprocess.run(
            ["node", script_path],
            capture_output=True, text=True, timeout=120,
        )
        Path(script_path).unlink(missing_ok=True)
        if result.returncode == 0 and result.stdout.strip():
            return result.stdout.strip()
        print(f"[push-gh-L3] Node.js script failed: {result.stderr[:400]}", file=sys.stderr)
        return None
    except Exception as e:
        print(f"[push-gh-L3] Node.js error: {type(e).__name__}: {e}", file=sys.stderr)
        return None


