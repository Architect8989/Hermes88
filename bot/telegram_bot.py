def rotate_camofox_key(
    hf_token: str,
    hf_space_id: str,
    env_path: str = "/data/.hermes/.env",
    skip_docker: bool = False,
) -> dict:
    """
    Rotate CAMOFOX_ACCESS_KEY without requiring VPS SSH access.

    Steps:
      1. Generate a cryptographically secure 64-char hex key via secrets.token_hex(32).
      2. Update env_path (/data/.hermes/.env) in-place, adding the key if absent.
      3. PUT the new key to the HuggingFace Spaces secrets API.
      4. docker restart camofox  (skipped if camofox is not running or skip_docker=True).

    Returns dict with keys:
      success         bool
      new_key_preview str   — first 8 chars + "..." (never the full key)
      steps           dict  — per-step outcome: env_updated, hf_secret_updated, docker_restarted
      error           str   — populated only on failure
    """
    import secrets as _secrets

    new_key  = _secrets.token_hex(32)          # 64 hex chars = 256-bit entropy
    preview  = new_key[:8] + "..."
    steps: dict[str, bool | str] = {
        "env_updated":      False,
        "hf_secret_updated": False,
        "docker_restarted": False,
    }

    # ── Step 1: Update local .env ───────────────────────────────────────────
    env_file = Path(env_path)
    try:
        if env_file.exists():
            lines      = env_file.read_text().splitlines()
            found      = False
            new_lines  = []
            for line in lines:
                if line.startswith("CAMOFOX_ACCESS_KEY="):
                    new_lines.append(f"CAMOFOX_ACCESS_KEY={new_key}")
                    found = True
                else:
                    new_lines.append(line)
            if not found:
                new_lines.append(f"CAMOFOX_ACCESS_KEY={new_key}")
            env_file.write_text("\n".join(new_lines) + "\n")
        else:
            env_file.parent.mkdir(parents=True, exist_ok=True)
            with env_file.open("a") as fh:
                fh.write(f"\nCAMOFOX_ACCESS_KEY={new_key}\n")
        steps["env_updated"] = True
        print(f"[rotate-camofox] .env updated at {env_path}", file=sys.stderr)
    except Exception as e:
        steps["env_updated"] = f"FAILED: {e}"
        print(f"[rotate-camofox] .env update failed: {e}", file=sys.stderr)

    # ── Step 2: Push secret to HuggingFace Spaces API ──────────────────────
    # HF Spaces secrets endpoint: PUT /api/spaces/{owner}/{space}/secrets
    # Body: {"key": "SECRET_NAME", "value": "secret_value"}
    # Auth: Bearer <hf_token>
    if hf_token and hf_space_id:
        hf_url = f"https://huggingface.co/api/spaces/{hf_space_id}/secrets"
        try:
            resp = requests.put(
                hf_url,
                headers={
                    "Authorization": f"Bearer {hf_token}",
                    "Content-Type":  "application/json",
                },
                json={"key": "CAMOFOX_ACCESS_KEY", "value": new_key},
                timeout=20,
            )
            if resp.status_code in (200, 201, 204):
                steps["hf_secret_updated"] = True
                print(
                    f"[rotate-camofox] HF Space secret updated "
                    f"(HTTP {resp.status_code})",
                    file=sys.stderr,
                )
            else:
                steps["hf_secret_updated"] = (
                    f"FAILED: HTTP {resp.status_code} — {resp.text[:200]}"
                )
                print(
                    f"[rotate-camofox] HF API returned {resp.status_code}: "
                    f"{resp.text[:200]}",
                    file=sys.stderr,
                )
        except Exception as e:
            steps["hf_secret_updated"] = f"FAILED: {e}"
            print(f"[rotate-camofox] HF secret update failed: {e}", file=sys.stderr)
    else:
        steps["hf_secret_updated"] = "SKIPPED: no hf_token or hf_space_id provided"
        print("[rotate-camofox] Skipping HF secret update (no token/space_id)", file=sys.stderr)

    # ── Step 3: docker restart camofox ─────────────────────────────────────
    if skip_docker:
        steps["docker_restarted"] = "SKIPPED: --skip-docker flag set"
    else:
        try:
            # Check camofox container exists before attempting restart
            check = subprocess.run(
                ["docker", "inspect", "--format", "{{.State.Status}}", "camofox"],
                capture_output=True, text=True, timeout=10,
            )
            if check.returncode != 0:
                steps["docker_restarted"] = "SKIPPED: camofox container not found"
                print(
                    "[rotate-camofox] camofox container not found — skipping restart",
                    file=sys.stderr,
                )
            else:
                container_status = check.stdout.strip()
                restart_r = subprocess.run(
                    ["docker", "restart", "camofox"],
                    capture_output=True, text=True, timeout=60,
                )
                if restart_r.returncode == 0:
                    steps["docker_restarted"] = True
                    print(
                        f"[rotate-camofox] docker restart camofox OK "
                        f"(was: {container_status})",
                        file=sys.stderr,
                    )
                else:
                    steps["docker_restarted"] = (
                        f"FAILED: rc={restart_r.returncode} — "
                        f"{restart_r.stderr[:200]}"
                    )
                    print(
                        f"[rotate-camofox] docker restart failed: "
                        f"{restart_r.stderr[:200]}",
                        file=sys.stderr,
                    )
        except FileNotFoundError:
            steps["docker_restarted"] = "SKIPPED: docker not in PATH"
            print("[rotate-camofox] docker not found — skipping restart", file=sys.stderr)
        except subprocess.TimeoutExpired:
            steps["docker_restarted"] = "FAILED: docker restart timed out after 60s"
            print("[rotate-camofox] docker restart timed out", file=sys.stderr)
        except Exception as e:
            steps["docker_restarted"] = f"FAILED: {e}"
            print(f"[rotate-camofox] docker restart error: {e}", file=sys.stderr)

    # ── Determine overall success ───────────────────────────────────────────
    # Rotation is successful if .env was updated. HF + docker are best-effort.
    env_ok = steps["env_updated"] is True
    success = env_ok

    if not success:
        return {
            "success":         False,
            "new_key_preview": preview,
            "steps":           steps,
            "error":           f"env update failed: {steps['env_updated']}",
        }

    return {
        "success":         True,
        "new_key_preview": preview,
        "steps":           steps,
    }


# ─────────────────────────────────────────────────────────────────────────────
# Stack Health Check  (FIX-J — v4.1)
# ─────────────────────────────────────────────────────────────────────────────

def health_check(
    gateway_url:   str = "",   # unused — hermes-gateway is a polling bot with no HTTP port
    grpc_host:     str = "localhost",
    grpc_port:     int = 50051,
    camofox_url:   str = "",   # resolved from CAMOFOX_HOST env at runtime
    jcode_port:    int = 18789,
    timeout:       float = 8.0,
) -> dict:
    """
    Probe all four Hermes stack services in parallel and return a status card.

    Services probed:
      hermes-gateway  — HTTP GET gateway_url  (default: localhost:8000/health)
      openclaude-grpc — gRPC channel ready    (default: localhost:50051)
      camofox         — HTTP GET camofox_url  (default: localhost:9377/health)
      jcode           — TCP connect to port   (default: localhost:18789)
                        falls back to process name scan if TCP fails

    Returns dict:
      all_healthy   bool
      checked_at    str   — ISO-8601 UTC timestamp
      services      dict  — per-service: { status, latency_ms, detail }
      summary       str   — human-readable one-liner for Telegram
    """
    import threading
    import socket
    import time as _time

    results: dict[str, dict] = {}
    lock = threading.Lock()

    def _record(name: str, status: str, latency_ms: float, detail: str = "") -> None:
        with lock:
            results[name] = {
                "status":     status,
                "latency_ms": round(latency_ms, 1),
                "detail":     detail,
            }

    # ── Probe: hermes-gateway (supervisorctl + process — it's a polling bot, NO HTTP port) ──
    def probe_gateway() -> None:
        t0 = _time.monotonic()
        # Method 1: supervisorctl status
        try:
            r = subprocess.run(
                ["supervisorctl", "-c", "/etc/supervisor/conf.d/rhodawk.conf",
                 "status", "hermes-gateway"],
                capture_output=True, text=True, timeout=timeout,
            )
            ms = (_time.monotonic() - t0) * 1000
            out = (r.stdout + r.stderr).strip()
            if "RUNNING" in out:
                pid_match = __import__("re").search(r"pid (\d+)", out)
                pid_info = f"pid {pid_match.group(1)}" if pid_match else ""
                _record("hermes-gateway", "UP", ms, f"supervisord: RUNNING {pid_info}".strip())
                return
            elif "STOPPED" in out or "FATAL" in out or "EXITED" in out:
                _record("hermes-gateway", "DOWN", ms, f"supervisord: {out[:120]}")
                return
        except Exception:
            pass

        # Method 2: find the gateway python process
        try:
            ps_r = subprocess.run(
                ["pgrep", "-f", "gateway/run.py"],
                capture_output=True, text=True, timeout=5,
            )
            ms = (_time.monotonic() - t0) * 1000
            if ps_r.returncode == 0:
                pids = ps_r.stdout.strip().replace("\n", ",")
                _record("hermes-gateway", "UP", ms, f"process running (pids: {pids[:60]})")
            else:
                _record("hermes-gateway", "DOWN", ms, "No gateway/run.py process found")
        except Exception as e2:
            ms = (_time.monotonic() - t0) * 1000
            _record("hermes-gateway", "DOWN", ms, f"probe failed: {str(e2)[:100]}")

    # ── Probe: openclaude gRPC (:50051) ──────────────────────────────────────
    def probe_grpc() -> None:
        t0 = _time.monotonic()
        try:
            import sys as _sys
            _sys.path.insert(0, "/app/openclaude_grpc")
            import grpc
            channel = grpc.insecure_channel(f"{grpc_host}:{grpc_port}")
            future  = grpc.channel_ready_future(channel)
            future.result(timeout=timeout)
            ms = (_time.monotonic() - t0) * 1000
            channel.close()
            _record("openclaude-grpc", "UP", ms, f"gRPC ready on :{grpc_port}")
        except Exception as e:
            ms = (_time.monotonic() - t0) * 1000
            err = str(e)[:120]
            # Distinguish "not reachable" from "grpc not installed"
            if "ModuleNotFoundError" in type(e).__name__ or "No module" in err:
                _record("openclaude-grpc", "UNKNOWN", ms, "grpc module not available in this env")
            else:
                _record("openclaude-grpc", "DOWN", ms, err)

    # ── Probe: camofox (HTTP — separate Docker container, use service DNS) ───
    def probe_camofox() -> None:
        # camofox is a SIBLING Docker container — never reachable on localhost.
        # Use CAMOFOX_HOST env (set to "camofox" by docker-compose) for DNS.
        camofox_host = os.environ.get("CAMOFOX_HOST", "camofox")
        camofox_port = os.environ.get("CAMOFOX_PORT", "9377")
        effective_url = camofox_url or f"http://{camofox_host}:{camofox_port}/health"
        t0 = _time.monotonic()
        try:
            r = requests.get(effective_url, timeout=timeout)
            ms = (_time.monotonic() - t0) * 1000
            if r.status_code < 400:
                body = r.text[:120].strip()
                _record("camofox", "UP", ms, f"HTTP {r.status_code} — {body}")
            else:
                _record("camofox", "DEGRADED", ms, f"HTTP {r.status_code}")
        except requests.exceptions.ConnectionError:
            ms = (_time.monotonic() - t0) * 1000
            _record("camofox", "DOWN", ms,
                    f"Connection refused at {effective_url} — container not running or still starting")
        except requests.exceptions.Timeout:
            ms = (_time.monotonic() - t0) * 1000
            _record("camofox", "DOWN", ms, f"Timed out after {timeout}s")
        except Exception as e:
            ms = (_time.monotonic() - t0) * 1000
            _record("camofox", "DOWN", ms, str(e)[:120])

    # ── Probe: jcode (TCP :18789 → fallback process scan) ───────────────────
    def probe_jcode() -> None:
        t0 = _time.monotonic()
        # Step 1: TCP connect
        try:
            sock = socket.create_connection(("localhost", jcode_port), timeout=timeout)
            sock.close()
            ms = (_time.monotonic() - t0) * 1000
            _record("jcode", "UP", ms, f"TCP :{jcode_port} open")
            return
        except (ConnectionRefusedError, socket.timeout, OSError):
            pass

        # Step 2: process name fallback (works when jcode doesn't open a TCP port)
        try:
            ps_r = subprocess.run(
                ["pgrep", "-f", "jcode"],
                capture_output=True, text=True, timeout=5,
            )
            ms = (_time.monotonic() - t0) * 1000
            if ps_r.returncode == 0:
                pids = ps_r.stdout.strip().replace("\n", ",")
                _record("jcode", "UP", ms, f"process running (pids: {pids[:60]})")
            else:
                # Step 3: check openclaw port :18789 via ss/netstat (alternative)
                ss_r = subprocess.run(
                    ["ss", "-tlnp"],
                    capture_output=True, text=True, timeout=5,
                )
                if str(jcode_port) in ss_r.stdout:
                    _record("jcode", "UP", ms, f"port :{jcode_port} listening (ss)")
                else:
                    _record("jcode", "DOWN", ms,
                            f"TCP :{jcode_port} closed, no jcode process found")
        except FileNotFoundError:
            ms = (_time.monotonic() - t0) * 1000
            _record("jcode", "UNKNOWN", ms, "pgrep not available — cannot probe process")
        except Exception as e:
            ms = (_time.monotonic() - t0) * 1000
            _record("jcode", "UNKNOWN", ms, str(e)[:120])

    # ── Run all probes in parallel ────────────────────────────────────────────
    import datetime as _dt
    checked_at = _dt.datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%SZ")

    threads = [
        threading.Thread(target=probe_gateway,  daemon=True),
        threading.Thread(target=probe_grpc,     daemon=True),
        threading.Thread(target=probe_camofox,  daemon=True),
        threading.Thread(target=probe_jcode,    daemon=True),
    ]
    for t in threads:
        t.start()
    # Wait up to timeout + 1s for all probes to finish
    for t in threads:
        t.join(timeout=timeout + 1)

    # ── Build summary ─────────────────────────────────────────────────────────
    status_order = {"UP": 0, "DEGRADED": 1, "UNKNOWN": 2, "DOWN": 3}
    all_healthy  = all(
        results.get(svc, {}).get("status") == "UP"
        for svc in ("hermes-gateway", "openclaude-grpc", "camofox", "jcode")
    )

    def _icon(status: str) -> str:
        return {"UP": "OK", "DEGRADED": "DEGRADED", "UNKNOWN": "UNKNOWN", "DOWN": "DOWN"}.get(
            status, "UNKNOWN"
        )

    lines = [f"Stack health @ {checked_at}"]
    for svc in ("hermes-gateway", "openclaude-grpc", "camofox", "jcode"):
        r = results.get(svc, {"status": "UNKNOWN", "latency_ms": 0, "detail": "probe did not run"})
        icon   = _icon(r["status"])
        lines.append(f"  {icon:8s}  {svc:20s}  {r['latency_ms']:6.1f}ms  {r['detail']}")

    summary = "\n".join(lines)
    overall = "ALL HEALTHY" if all_healthy else "DEGRADED — see details"
    summary += f"\n\nOverall: {overall}"

    print(summary, file=sys.stderr)

    return {
        "all_healthy": all_healthy,
        "checked_at":  checked_at,
        "services":    results,
        "summary":     summary,
    }


